    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        char[] passwordCharArray = passwordField2.getPassword();
        String password = String.valueOf(passwordCharArray);
        char[] passwordCharArray1 = passwordField.getPassword();
        String new1 = String.valueOf(passwordCharArray1);
        char[] passwordCharArray2 = passwordField1.getPassword();
        String new2 = String.valueOf(passwordCharArray2);

        if (password.equals(account.getPassword())) {
            if (!new1.equals("")) {
                if (new1.equals(new2)) {
                    account.setPassword(new1);
                    JOptionPane.showMessageDialog(null, "Password updated successfully!");
                    DB4OUtil.getInstance().storeSystem(system);
                    resetPasswordField();
                } else {
                    JOptionPane.showMessageDialog(null, "Passwords don't match!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password can't be empty!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Password is not correct!");
        }
        DB4OUtil.getInstance().storeSystem(system);

    }//GEN-LAST:event_submitButtonActionPerformed

    private void AssignjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssignjButtonActionPerformed
        int selectedRow = UnallocatedOrderjTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Commodity order = (Commodity) UnallocatedOrderjTable.getValueAt(selectedRow, 2);
        if (order.getStatus() == Status.Waiting) {
            order.setStatus(Status.Sending);
            order.setReceiver(account);
            account.getWorkQueue().getWorkRequestList().add(order);
            order.setDeliveryManName(account.getPerson().returnFullName());
            order.setRequestDate(new Date());
            order.setDeliveryManPhone(((Employee) account.getPerson()).getPhone());
            order.getOrder().setDeliverymanName(order.getDeliveryManName());
             DB4OUtil.getInstance().storeSystem(system);
            populateUnAllocatedDeliveryMan();
            populateAllocatedDeliveryMan();
        } else {
            JOptionPane.showMessageDialog(null, "the order can not be delivered again");
        }
    }//GEN-LAST:event_AssignjButtonActionPerformed

    private void DeliveredjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeliveredjButtonActionPerformed
        int selectedRow = AllocatedOrderjTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Commodity order = (Commodity) AllocatedOrderjTable.getValueAt(selectedRow, 2);
        if (order.getStatus() == Status.Sending) {
            order.setStatus(Status.Delivery);
            order.setResolveDate(new Date());
            order.getOrder().setStatus(Status.Delivery);
             DB4OUtil.getInstance().storeSystem(system);
            populateUnAllocatedDeliveryMan();
            populateAllocatedDeliveryMan();
        } else {
            JOptionPane.showMessageDialog(null, "the order can not be received again");
        }
    }//GEN-LAST:event_DeliveredjButtonActionPerformed


