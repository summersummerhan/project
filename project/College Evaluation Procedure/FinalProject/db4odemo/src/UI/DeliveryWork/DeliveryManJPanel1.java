    private void populateUnAllocatedDeliveryMan() {
        DefaultTableModel dtm = (DefaultTableModel) UnallocatedOrderjTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof Commodity) {
                Object[] row = new Object[5];
                row[0] = ((Commodity) wr).getCustomerAddress();
                row[1] = ((Commodity) wr).getCustomerPhone();
                row[2] = wr;
                if (wr.getRequestDate() != null) {
                    row[3] = wr.dataFormat(wr.getRequestDate());
                }
                if (wr.getResolveDate() != null) {
                    row[4] = wr.dataFormat(wr.getResolveDate());
                }
                dtm.addRow(row);
            }
        }
    }

    private void populateAllocatedDeliveryMan() {
        DefaultTableModel dtm = (DefaultTableModel) AllocatedOrderjTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest wr : account.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof Commodity) {
                Object[] row = new Object[5];
                row[0] = ((Commodity) wr).getCustomerAddress();
                row[1] = ((Commodity) wr).getCustomerPhone();
                row[2] = wr;
                if (wr.getRequestDate() != null) {
                    row[3] = wr.dataFormat(wr.getRequestDate());
                }
                if (wr.getResolveDate() != null) {
                    row[4] = wr.dataFormat(wr.getResolveDate());
                }
                dtm.addRow(row);
            }
        }
    }
